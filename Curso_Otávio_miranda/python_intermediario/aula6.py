# conversão de tipos, coerção
# type convertion, typecasting, coercion
# é o ato de converter um tipo em outro
#  tipos imutáveis e primitivos:
# str, int, float, bool
print(1 + 1)
print('a' + 'b')
print(int('11') + 12)
print(str(65) + 'a')
print(bool(' '))
print(type(56))
print(type(1.2))
print(type(True))
print(type('Austrália'))